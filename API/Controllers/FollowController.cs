using Application.Features.Commands.Followers.Follow;
using Application.Features.Queries.Followers.GetFollowers;
using Microsoft.AspNetCore.Mvc;

namespace API.Controllers
{
    public class FollowController : BaseApiController
    {
        [HttpPost("{username}")]
        public async Task<IActionResult> Follow(string username)
        {
            return HandleResult(await Mediator.Send(new FollowToggleCommandRequest { TargetUsername = username }));
        }

        [HttpGet("{username}")]
        public async Task<IActionResult> GetFollowings(string username, string predicate)
        {
            return HandleResult(await Mediator.Send(new GetFollowersQueryRequest { Username = username, Predicate = predicate }));
        }
    }
}